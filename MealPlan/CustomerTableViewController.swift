    override func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCell(withIdentifier: "CustomerCell", for: indexPath)

        // Configure the cell...
        let customer = customers[indexPath.row]
        
        let dateFormatter = DateFormatter()
        dateFormatter.dateStyle = .medium
        
        cell.textLabel?.text = "\(customer.firstName) \(customer.lastName)"
        cell.detailTextLabel?.text = "\(dateFormatter.string(from: customer.startDate)) - \(dateFormatter.string(from: customer.endDate))"

        return cell
    }
    
    @IBAction func unwindFromAddMealPlan(unwindSegue: UIStoryboardSegue) {
        
